# Tipos Int e Float
# int -> Numero Inteiro
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado positivo

print(11) #int
print(-11) #int
print(0)

# float -> Número com ponto flutuante
# O tipo flaot representa qualquer número
# Positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo
print(1.1, 10.11)

# A função type mostra o tipo que o Python
# Inferiou ao valor
print( type('Teste') ) # type é uma classe. 'é callable' 
# Tudo em Python é um objeto
print( type('-1') )
print(type(1.1))
print(type(1.1), type('teste'), type(2))
